    R.sort()
    A=[R[0]]
    for i in range(1,2*n):
        if R[i]!=R[i-1]:
            A.append(R[i])
    
    m=len(A)
    diff=[0 for _ in range(m+1)]

    for i in range(n):
        a=binsearch(A,I[i][0])
        b=binsearch(A,I[i][1])
        diff[a]+=1
        diff[b+1]-=1

    maxi=diff[0]

    for i in range(1,m+1):
        diff[i]+=diff[i-1]
        maxi=max(maxi,diff[i])

    return maxi



